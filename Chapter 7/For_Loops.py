# For loop using range function
for i in range(1,7) :
    print(i)

# For loop traversing in list
l=[1,6,546.6,"SAM",False]
for i in l :
    print(i)

# For loop traversing in Tuple
t=(2,6,78,5466.6,"SA",True)
for i in t :
    print(i)

# For loop traversing in String
s="SUMAN"
for i in s :
    print(i)